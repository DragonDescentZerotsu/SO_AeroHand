def set_joint(planner: PlanningModel, qpos: np.ndarray, joint_name: str, value: float) -> None:
    jid = planner.joint_id(joint_name)
    qpos[int(planner.model.jnt_qposadr[jid])] = float(value)


def set_left_gripper(planner: PlanningModel, qpos: np.ndarray, opening: float) -> None:
    opening = float(np.clip(opening, 0.0, 0.035))
    set_joint(planner, qpos, LEFT_GRIPPER_JOINTS[0], opening)
    set_joint(planner, qpos, LEFT_GRIPPER_JOINTS[1], -opening)


def actuator_index(model, name: str) -> int | None:
    aid = mujoco.mj_name2id(model, mujoco.mjtObj.mjOBJ_ACTUATOR, name)
    return None if aid < 0 else int(aid)


def set_ctrl_for_q(planner: PlanningModel, ctrl: np.ndarray, group: JointGroup, q: np.ndarray) -> None:
    for joint_name, value in zip(group.names, q, strict=True):
        aid = actuator_index(planner.model, joint_name)
        if aid is not None:
            ctrl[aid] = float(value)


def interpolate_path(path: np.ndarray, max_step: float = 0.035) -> np.ndarray:
    dense: list[np.ndarray] = []
    for start, end in zip(path[:-1], path[1:], strict=False):
        if not dense:
            dense.append(start.copy())
        distance = float(np.linalg.norm(end - start))
        steps = max(2, int(np.ceil(distance / max(max_step, 1e-6))) + 1)
        for alpha in np.linspace(0.0, 1.0, steps)[1:]:
            dense.append((1.0 - alpha) * start + alpha * end)
    if not dense:
        dense.append(path[0].copy())
    return np.asarray(dense, dtype=np.float64)


def make_qpos_from_arm(planner: PlanningModel, group: JointGroup, template: np.ndarray, q: np.ndarray) -> np.ndarray:
    qpos = template.copy()
    planner.set_joint_group(qpos, group, q)
    return qpos


def is_valid_q(
    planner: PlanningModel,
    group: JointGroup,
    template: np.ndarray,
    q: np.ndarray,
    policy: CollisionPolicy,
) -> bool:
    qpos = make_qpos_from_arm(planner, group, template, q)
    planner.forward(qpos)
    return not any(policy.is_forbidden(*pair) for pair in planner.contact_pairs())


def make_rrt(
    planner: PlanningModel,
    group: JointGroup,
    template: np.ndarray,
    policy: CollisionPolicy,
    *,
    seed: int,
) -> RRTConnectPlanner:
    def state_valid(q: np.ndarray) -> bool:
        return is_valid_q(planner, group, template, q, policy)

    return RRTConnectPlanner(
        group.lower,
        group.upper,
        state_valid,
        config=RRTConnectConfig(
            step_size=0.11,
            edge_resolution=0.025,
            max_iterations=7000,
            timeout_s=10.0,
            shortcut_attempts=160,
            seed=seed,
        ),
    )


def gripper_center_offset_local(planner: PlanningModel, qpos: np.ndarray) -> np.ndarray:
    planner.forward(qpos)
    link6_id = planner.body_id("piper_original/link6")
    link7_id = planner.body_id("piper_original/link7")
    link8_id = planner.body_id("piper_original/link8")
    link6_pos = planner.data.xpos[link6_id].copy()
    link6_R = planner.data.xmat[link6_id].reshape(3, 3).copy()
    center = 0.5 * (planner.data.xpos[link7_id] + planner.data.xpos[link8_id])
    return link6_R.T @ (center - link6_pos)


def target_link6_pose(tcp_pos: np.ndarray, tcp_R: np.ndarray, tcp_offset_local: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    return tcp_pos - tcp_R @ tcp_offset_local, tcp_R


def level_side_grasp_frames(pipette_axis: np.ndarray, reference_R: np.ndarray) -> list[np.ndarray]:
    """Return equivalent side-grasp frames with local Y parallel to the ground."""

    world_z = np.array([0.0, 0.0, 1.0], dtype=np.float64)
    frames: list[np.ndarray] = []
    for x_sign in (1.0, -1.0):
        x_axis = x_sign * normalize(pipette_axis)
        y_axis = np.cross(x_axis, world_z)
        if float(np.linalg.norm(y_axis)) < 1e-8:
            y_axis = reference_R[:, 1] - world_z * float(np.dot(reference_R[:, 1], world_z))
        y_axis = normalize(y_axis)
        for y_sign in (1.0, -1.0):
            candidate_y = y_sign * y_axis
            z_axis = normalize(np.cross(x_axis, candidate_y))
            frames.append(np.stack([x_axis, candidate_y, z_axis], axis=1))
    return sorted(
        frames,
        key=lambda frame: float(Rotation.from_matrix(frame @ reference_R.T).magnitude()),
    )


def level_gripper_y_path(
    planner: PlanningModel,
    group: JointGroup,
    template: np.ndarray,
    path: np.ndarray,
    collision_policy: CollisionPolicy,
    *,
    max_step: float = 0.02,
) -> np.ndarray:
    """Use wrist roll to keep link6 local Y horizontal along a joint path."""

    dense_path = interpolate_path(np.asarray(path, dtype=np.float64), max_step=max_step)
    link6_id = planner.body_id("piper_original/link6")
    leveled: list[np.ndarray] = []
    previous_roll = float(dense_path[0, -1])

    for q in dense_path:
        candidate = q.copy()
        candidate[-1] = 0.0
        qpos = make_qpos_from_arm(planner, group, template, candidate)
        planner.forward(qpos)
        zero_roll_R = planner.data.xmat[link6_id].reshape(3, 3)

        # For R(q6) = R(0) RotZ(q6), solve world_z dot local_Y(q6) = 0.
        base_roll = float(np.arctan2(zero_roll_R[2, 1], zero_roll_R[2, 0]))
        roll_candidates = [
            base_roll + k * np.pi
            for k in range(-3, 4)
            if group.lower[-1] <= base_roll + k * np.pi <= group.upper[-1]
        ]
        if not roll_candidates:
            raise RuntimeError("No joint6 value can keep the gripper local Y axis horizontal")
        candidate[-1] = min(roll_candidates, key=lambda value: abs(value - previous_roll))
        previous_roll = float(candidate[-1])

        qpos = make_qpos_from_arm(planner, group, template, candidate)
        planner.forward(qpos)
        if any(collision_policy.is_forbidden(*pair) for pair in planner.contact_pairs()):
            raise RuntimeError("Level-wrist path introduced a forbidden collision")
        leveled.append(candidate)

    return np.asarray(leveled, dtype=np.float64)


def build_candidate_frames(target_R: np.ndarray, roll_axis: np.ndarray) -> list[np.ndarray]:
    _ = roll_axis
    return [target_R]


def find_stage_ik(
    planner: PlanningModel,
    group: JointGroup,
    template: np.ndarray,
    link6_body: str,
    tcp_pos: np.ndarray,
    target_R: np.ndarray,
    roll_axis: np.ndarray,
    tcp_offset_local: np.ndarray,
    seed_q: np.ndarray,
    collision_policy: CollisionPolicy | None,
    rng: np.random.Generator,
    max_tcp_error: float = 0.075,
    ik_regularization: float = 0.03,
) -> tuple[np.ndarray, np.ndarray, float]:
    random_seeds = [
        rng.uniform(group.lower, group.upper),
        np.clip(seed_q + rng.normal(0.0, 0.35, size=seed_q.shape), group.lower, group.upper),
        LEFT_HOME,
    ]
    best: tuple[np.ndarray, np.ndarray, float, float] | None = None
    for candidate_R in build_candidate_frames(target_R, roll_axis):
        target_pos, target_link6_R = target_link6_pose(tcp_pos, candidate_R, tcp_offset_local)
        try:
            q, cost = solve_body_pose_ik(
                planner,
                group,
                link6_body,
                target_pos,
                target_link6_R,
                seed=seed_q,
                extra_seeds=random_seeds,
                collision_policy=collision_policy,
                fixed_qpos=template,
                regularization_weight=ik_regularization,
            )
        except RuntimeError:
            continue
        qpos = make_qpos_from_arm(planner, group, template, q)
        planner.forward(qpos)
        link6_pos = planner.data.xpos[planner.body_id(link6_body)].copy()
        link6_R = planner.data.xmat[planner.body_id(link6_body)].reshape(3, 3).copy()
        tcp_actual = link6_pos + link6_R @ tcp_offset_local
        tcp_error = float(np.linalg.norm(tcp_actual - tcp_pos))
        continuity_error = float(np.linalg.norm(q - seed_q))
        wrist_roll_error = abs(float(q[-1] - seed_q[-1]))
        score = tcp_error + 0.01 * cost + 0.02 * continuity_error + 0.08 * wrist_roll_error
        if best is None or score < best[3]:
            best = (q, candidate_R, tcp_error, score)
    if best is None:
        raise RuntimeError("No IK candidate solved")
    if best[2] > max_tcp_error:
        raise RuntimeError(f"No IK candidate reached the TCP target closely enough: error={best[2]:.4f}m")
    return best[0], best[1], best[2]


def plan_cartesian_tcp_path(
    planner: PlanningModel,
    group: JointGroup,
    template: np.ndarray,
    link6_body: str,
    start_tcp_pos: np.ndarray,
    end_tcp_pos: np.ndarray,
    target_R: np.ndarray,
    tcp_offset_local: np.ndarray,
    start_q: np.ndarray,
    collision_policy: CollisionPolicy | None,
    *,
    max_tcp_step: float = 0.015,
    max_tcp_error: float = 0.035,
    regularization_weight: float = 0.35,
) -> tuple[np.ndarray, float]:
    """Build a continuous Cartesian TCP path with fixed gripper orientation."""

    start_tcp_pos = np.asarray(start_tcp_pos, dtype=np.float64).reshape(3)
    end_tcp_pos = np.asarray(end_tcp_pos, dtype=np.float64).reshape(3)
    distance = float(np.linalg.norm(end_tcp_pos - start_tcp_pos))
    steps = max(2, int(np.ceil(distance / max(max_tcp_step, 1e-6))) + 1)
    path = [np.asarray(start_q, dtype=np.float64).copy()]
    worst_error = 0.0
    seed_q = path[-1]
    target_link6_R = np.asarray(target_R, dtype=np.float64).reshape(3, 3)

    for alpha in np.linspace(0.0, 1.0, steps)[1:]:
        tcp_pos = (1.0 - alpha) * start_tcp_pos + alpha * end_tcp_pos
        target_pos, target_body_R = target_link6_pose(tcp_pos, target_link6_R, tcp_offset_local)
        try:
            q, _ = solve_body_pose_ik(
                planner,
                group,
                link6_body,
                target_pos,
                target_body_R,
                seed=seed_q,
                collision_policy=collision_policy,
                fixed_qpos=template,
                regularization_weight=regularization_weight,
                max_nfev=350,
            )
        except RuntimeError as exc:
            raise RuntimeError(
                "Cartesian IK failed "
                f"at alpha={alpha:.3f}, tcp_pos={tcp_pos.tolist()}"
            ) from exc
        qpos = make_qpos_from_arm(planner, group, template, q)
        planner.forward(qpos)
        link6_pos = planner.data.xpos[planner.body_id(link6_body)].copy()
        link6_R = planner.data.xmat[planner.body_id(link6_body)].reshape(3, 3).copy()
        tcp_actual = link6_pos + link6_R @ tcp_offset_local
        tcp_error = float(np.linalg.norm(tcp_actual - tcp_pos))
        if tcp_error > max_tcp_error:
            raise RuntimeError(f"Cartesian IK waypoint missed TCP target: error={tcp_error:.4f}m")
        worst_error = max(worst_error, tcp_error)
        path.append(q.copy())
        seed_q = q

    return np.asarray(path, dtype=np.float64), worst_error


def plan_cartesian_tcp_pose_path(
    planner: PlanningModel,
    group: JointGroup,
    template: np.ndarray,
    link6_body: str,
    start_tcp_pos: np.ndarray,
    end_tcp_pos: np.ndarray,
    start_R: np.ndarray,
    end_R: np.ndarray,
    tcp_offset_local: np.ndarray,
    start_q: np.ndarray,
    collision_policy: CollisionPolicy | None,
    *,
    max_tcp_step: float = 0.015,
    max_rotation_step_rad: float = np.deg2rad(5.0),
    max_tcp_error: float = 0.05,
    regularization_weight: float = 0.35,
) -> tuple[np.ndarray, float]:
    """Build a continuous Cartesian path with shortest-arc orientation interpolation."""

    start_tcp_pos = np.asarray(start_tcp_pos, dtype=np.float64).reshape(3)
    end_tcp_pos = np.asarray(end_tcp_pos, dtype=np.float64).reshape(3)
    start_R = np.asarray(start_R, dtype=np.float64).reshape(3, 3)
    end_R = np.asarray(end_R, dtype=np.float64).reshape(3, 3)
    translation_distance = float(np.linalg.norm(end_tcp_pos - start_tcp_pos))
    rotation_distance = float(Rotation.from_matrix(end_R @ start_R.T).magnitude())
    steps = max(
        2,
        int(np.ceil(translation_distance / max(max_tcp_step, 1e-6))) + 1,
        int(np.ceil(rotation_distance / max(max_rotation_step_rad, 1e-6))) + 1,
    )
    alphas = np.linspace(0.0, 1.0, steps)
    orientation_path = Slerp(
        [0.0, 1.0],
        Rotation.from_matrix(np.stack([start_R, end_R])),
    )(alphas).as_matrix()

    path = [np.asarray(start_q, dtype=np.float64).copy()]
    seed_q = path[-1]
    worst_error = 0.0
    for index, alpha in enumerate(alphas[1:], start=1):
        tcp_pos = (1.0 - alpha) * start_tcp_pos + alpha * end_tcp_pos
        target_R = orientation_path[index]
        target_pos, target_body_R = target_link6_pose(tcp_pos, target_R, tcp_offset_local)
        try:
            q, _ = solve_body_pose_ik(
                planner,
                group,
                link6_body,
                target_pos,
                target_body_R,
                seed=seed_q,
                collision_policy=collision_policy,
                fixed_qpos=template,
                regularization_weight=regularization_weight,
                max_nfev=400,
            )
        except RuntimeError as exc:
            raise RuntimeError(
                "Cartesian pose IK failed "
                f"at alpha={alpha:.3f}, tcp_pos={tcp_pos.tolist()}"
            ) from exc
        qpos = make_qpos_from_arm(planner, group, template, q)
        planner.forward(qpos)
        link6_pos = planner.data.xpos[planner.body_id(link6_body)].copy()
        link6_R = planner.data.xmat[planner.body_id(link6_body)].reshape(3, 3).copy()
        tcp_actual = link6_pos + link6_R @ tcp_offset_local
        tcp_error = float(np.linalg.norm(tcp_actual - tcp_pos))
        if tcp_error > max_tcp_error:
            raise RuntimeError(f"Cartesian pose IK waypoint missed TCP target: error={tcp_error:.4f}m")
        worst_error = max(worst_error, tcp_error)
        path.append(q.copy())
        seed_q = q

    return np.asarray(path, dtype=np.float64), worst_error


